import Foundation
import SwiftUI

extension View {
    /// Wraps view into an AnyView
    func eraseToAnyView() -> AnyView {
        AnyView(self)
    }

    /// Apply a view modifier conditionally
    @ViewBuilder func applyIf<T: View>(_ condition: @autoclosure () -> Bool, apply: (Self) -> T) -> some View {
        if condition() {
            apply(self)
        } else {
            self
        }
    }
}
